const chooseRecipe = function(bakeryA, bakeryB, recipes) {
  let setA = new Set(bakeryA);
  let setB = new Set(bakeryB);

  for(let recipe of recipes){
    let igr1 = recipe.ingredients[0];
    let igr2 = recipe.ingredients[1];
    if((setA.has(igr1) && setB.has(igr2)) 
    || (setA.has(igr2) && setB.has(igr1))) return recipe.name;
  }
  return null;
}

let bakeryA = ['saffron', 'eggs', 'tomato paste', 'coconut', 'custard'];
